export function appVisible(app, hasPermission) {
  if (app.permission) return hasPermission(app.permission);
  if (app.permissions?.length) {
    return app.permissions.some((p) => hasPermission(p));
  }
  return true;
}

export function filterAppsByPermission(apps, hasPermission) {
  return apps.filter((app) => appVisible(app, hasPermission));
}

export function groupAppsByCategory(apps) {
  const groups = {};
  for (const app of apps) {
    const key = app.category || "other";
    if (!groups[key]) groups[key] = [];
    groups[key].push(app);
  }
  return groups;
}
